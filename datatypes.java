public class Main {
    public static void main(String[] args) {
        int a = 25;
        double b = 3.14;
        char ch = 'A';
        boolean c = true;
        String s = "Hello Java!";

        System.out.println("Integer: " + a);
        System.out.println("Double: " + b);
        System.out.println("Char: " + ch);
        System.out.println("Boolean: " + c);
        System.out.println("String: " + s);
    }
}

